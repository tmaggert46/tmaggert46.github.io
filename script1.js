function updateChart(selectedClass) {
    const data = classData[selectedClass];
    
    // Update the x and y domains
    x.domain(data.map(d => d.name));
    y.domain([0, 100]).nice(); // Scale out of 100
    
    // Select all bars and bind data
    const bars = svg.selectAll(".bar")
        .data(data, d => d.name);

    bars.exit().remove();
    
    bars.enter()
        .append("rect")
        .attr("class", "bar")
        .attr("x", d => x(d.name))
        .attr("y", height) // Start from the bottom
        .attr("width", x.bandwidth())
        .attr("height", 0) // Start with 0 height
        .attr("fill", d => statColors[d.name])
        .merge(bars)
        .transition()
        .delay(250) // Wait for 0.25 seconds before starting the animation
        .duration(1000) // Duration of the animation
        .attr("x", d => x(d.name))
        .attr("y", d => y(d.value))
        .attr("width", x.bandwidth())
        .attr("height", d => height - y(d.value));
    
    // Add text elements to display bar values
    svg.selectAll(".bar-label").remove(); // Remove old labels if they exist

    svg.selectAll(".bar-label")
        .data(data)
        .enter()
        .append("text")
        .attr("class", "bar-label")
        .attr("x", d => x(d.name) + x.bandwidth() / 2)
        .attr("y", height) // Start from the bottom
        .attr("text-anchor", "middle")
        .merge(svg.selectAll(".bar-label"))
        .transition()
        .delay(250) // Wait for 0.25 seconds before starting the animation
        .duration(1000) // Duration of the animation
        .attr("x", d => x(d.name) + x.bandwidth() / 2)
        .attr("y", d => y(d.value) - 5) // Position above the bar
        .text(d => d.value.toFixed(0)); // Display rounded value
    
    // Update axes
    svg.select(".x-axis")
        .transition()
        .call(d3.axisBottom(x));
    
    svg.select(".y-axis")
        .transition()
        .call(d3.axisLeft(y));
}
    
