const margin = { top: 20, right: 30, bottom: 40, left: 40 };
const width = 800 - margin.left - margin.right;
const height = 500 - margin.top - margin.bottom;

const svg = d3.select("#chart")
    .append("svg")
    .attr("width", width + margin.left + margin.right)
    .attr("height", height + margin.top + margin.bottom)
    .append("g")
    .attr("transform", `translate(${margin.left},${margin.top})`);

const x = d3.scaleBand()
    .range([0, width])
    .padding(0.1);

const y = d3.scaleLinear()
    .range([height, 0]);

svg.append("g")
    .attr("class", "x-axis")
    .attr("transform", `translate(0,${height})`);

svg.append("g")
    .attr("class", "y-axis");

function updateChart(selectedCharacter) {
    const data = characterData[selectedCharacter];

    x.domain(data.map(d => d.name));
    y.domain([0, 100]).nice();

    svg.selectAll(".bar").remove();
    svg.selectAll(".bar-label").remove();

    svg.selectAll(".bar")
        .data(data)
        .enter()
        .append("rect")
        .attr("class", "bar")
        .attr("x", d => x(d.name))
        .attr("y", height)
        .attr("width", x.bandwidth())
        .attr("height", 0)
        .attr("fill", d => statColors[d.name])
        .transition()
        .delay(250)
        .duration(1000)
        .attr("y", d => y(d.value))
        .attr("height", d => height - y(d.value));

    svg.select(".x-axis")
        .call(d3.axisBottom(x));

    svg.select(".y-axis")
        .call(d3.axisLeft(y));

    // Add text elements to display bar values
    svg.selectAll(".bar-label")
        .data(data)
        .enter()
        .append("text")
        .attr("class", "bar-label")
        .attr("x", d => x(d.name) + x.bandwidth() / 2)
        .attr("y", height)
        .attr("text-anchor", "middle")
        .merge(svg.selectAll(".bar-label"))
        .transition()
        .delay(250)
        .duration(1000)
        .attr("x", d => x(d.name) + x.bandwidth() / 2)
        .attr("y", d => y(d.value) - 5)
        .text(d => d.value.toFixed(0));
}

function initializeDropdowns() {
    const classSelect = d3.select("#class-select");

    // List of classes to highlight
    const highlightedClasses = ["Etie", "Chloé", "Veyle"];

    // Add class options
    Object.keys(characterData).forEach(cls => {
        const option = classSelect.append("option")
            .attr("value", cls)
            .text(cls);
        
        // Highlight specified classes
        if (highlightedClasses.includes(cls)) {
            option.attr("class", "highlighted-option");
        }
    });
} 

updateChart("Alear");
initializeDropdowns();

// Add event listener to dropdown
d3.select("#class-select").on("change", function() {
    const selectedCharacter = d3.select(this).property("value");
    updateChart(selectedCharacter);
});

