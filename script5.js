// Function to calculate the final character stat
function calculateFinalCharacterStat(character) {
    const stats = characterData[character];
    const initial = initialStats[character];
    const finalStats = stats.map(stat => {
        const initialStat = initial[stat.name];
        const level = initial.Level;
        const finalStat = initialStat + (40 - level) * (stat.value / 100)
        return {
            name: stat.name,
            value: Math.round(finalStat)
        };
    });
    return finalStats;
}

function updateScatterplot() {
  const character = d3.select("#character-select").property("value");
  const selectedClass = d3.select("#class-select").property("value");

  const characterScores = calculateFinalCharacterStat(character);
  const classScores = classData[selectedClass].map(stat => ({
      name: stat.name,
      value: stat.value
  }));

  const data = characterScores.map(charStat => {
      const classStat = classScores.find(clsStat => clsStat.name === charStat.name);
      return {
          name: charStat.name,
          characterValue: charStat.value,
          classValue: classStat ? classStat.value : 0
      };
  });

  const margin = { top: 20, right: 20, bottom: 30, left: 40 };
  const width = 800 - margin.left - margin.right;
  const height = 500 - margin.top - margin.bottom;

  // Remove any existing SVG element
  d3.select("#scatterplot").select("svg").remove();

  const svg = d3.select("#scatterplot")
      .append("svg")
      .attr("width", width + margin.left + margin.right)
      .attr("height", height + margin.top + margin.bottom)
      .append("g")
      .attr("transform", `translate(${margin.left},${margin.top})`);

  const x = d3.scaleLinear()
      .domain([0, d3.max(characterScores, d => d.value)]) // Scale x-axis to range of character values
      .range([0, width]);

  const y = d3.scaleLinear()
      .domain([0, d3.max(classScores, d => d.value)]) // Scale y-axis to range of class values
      .range([height, 0]);

  svg.append("g")
      .attr("class", "x-axis")
      .attr("transform", `translate(0,${height})`)
      .call(d3.axisBottom(x));

  svg.append("g")
      .attr("class", "y-axis")
      .call(d3.axisLeft(y));

  // Draw the y=x line
  const xDomain = x.domain();

  svg.append("line")
      .attr("x1", x(xDomain[0])) // x value at the domain's start
      .attr("y1", y(xDomain[0])) // y value corresponding to the domain's start (should be the same as xDomain[0])
      .attr("x2", x(xDomain[1])) // x value at the domain's end
      .attr("y2", y(xDomain[1])) // y value corresponding to the domain's end (should be the same as xDomain[1])
      .attr("stroke", "gray")
      .attr("stroke-width", 1)
      .attr("stroke-dasharray", "4 2");

  svg.selectAll(".dot")
      .data(data)
      .enter()
      .append("circle")
      .attr("class", "dot")
      .attr("cx", d => x(d.characterValue))
      .attr("cy", d => y(d.classValue))
      .attr("r", 5)
      .attr("fill", d => statColors[d.name]);

  svg.selectAll(".text")
      .data(data)
      .enter()
      .append("text")
      .attr("x", d => x(d.characterValue))
      .attr("y", d => y(d.classValue))
      .attr("dy", -10)
      .attr("text-anchor", "middle")
      .text(d => d.name)
      .style("font-size", "10px");

      function calculateFitness(characterStat, classStat) {
        return characterStat.reduce((totalDistance, charStat) => {
            const classValue = classStat.find(stat => stat.name === charStat.name);
            if (classValue) {
                const predictedY = charStat.value; // Character value
                const actualY = classValue.value; // Class value
                const distance = Math.abs(predictedY - actualY); // Fitness measure
                return totalDistance + distance;
            }
            return totalDistance;
        }, 0) / characterStat.length;
    }

  const totalDistance = calculateFitness(characterScores, classScores);
  d3.select("#fit-display").text(totalDistance.toFixed(2));
}



